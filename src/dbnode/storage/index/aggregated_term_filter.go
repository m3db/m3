package index

import "bytes"

// Allow returns true if the given term satisfies the filter.
func (f AggregateTermFilter) Allow(term []byte) bool {
	if len(f) == 0 {
		// NB: if filter is empty, all values are valid.
		return true
	}

	for _, allowed := range f {
		if bytes.Equal(term, allowed) {
			return true
		}
	}

	return false
}
