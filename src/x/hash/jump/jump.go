package jump

// Hash maps a uint64 to a bucket in the range [0, numBuckets).
func Hash(key uint64, numBuckets int64) int64 {
	if numBuckets < 0 {
		return -1
	}

	var b, j int64
	for j < numBuckets {
		b = j
		key = key*2862933555777941757 + 1
		j = int64(float64(b+1) * (float64(int64(1)<<31) / float64((key>>33)+1)))
	}

	return b
}
